package com.thzhima.base;

public class Computer {
	
	 String brand;
	 String color;
	 String size;
	 int price;
	 
	 public Computer(String brand,String color,String size, int price) {
		 this.brand = brand;
		 this.color = color;
		 this.size = size;
		 this.price = price;
	 }
	 
	 void work() {
		 System.out.println(this.brand + " : 可以计算，听歌，放电影");
		
	 }
	 
	 public static void main(String[] args) {
		 Computer c = new Computer("联想小新","银色","1366x736",1000000);
		
		 System.out.println("品牌:"+c.brand + "颜色:"+ c.color + "大小:" +c.size + "价格:"+ c.price);
		 c.work();
	 }
}
